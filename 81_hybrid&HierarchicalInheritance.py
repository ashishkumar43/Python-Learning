# Hybrid and Hierarchical Inheritance 
# Hybrid inheritance --> It is a combination of multiple and single level inheritance only one base class with multiple derived class.
 
# Example of Hybrid inheritance:
class Baseclass:
    pass

class derived1(Baseclass):
    pass

class derived2(Baseclass):
    pass

class derived3(Baseclass):
    pass


# Hierarchial Inheritance  -- tree like structure

